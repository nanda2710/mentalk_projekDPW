                        <h4 class="card-title" style="text-align: left; color: #6aadaa;">Mental Health Awareness di
                            Indonesia</h4>
                        <img class="img-fluid" src="img/a.png" alt="Responsive image"
                            style="padding-right: 25px; display: block; margin: auto;" width="20%" height="20%">
                        <p class="card-text"
                            style="text-align: left; color: black; font-size: 16px; padding-top: 20px;">Pada tahun 2016,
                            Indonesia kembali menjadi sorotan dunia, namun dengan alasan yang tidak baik. Organisasi
                            Human Rights Watch menerbitkan laporan yang meng-expose realita yang terjadi terhadap
                            penyandang disabilitas psikososial di Indonesia. Laporan tersebut pun diikiuti dengan
                            rentetan berita dari media asing yang menampilkan foto-foto manusia yang dirantai dan
                            dikurung dalam ruang sempit, atau yang sering kita bilang sebagai “pasung”. “Pasung” adalah
                            praktik untuk membelenggu atau mengurung seseorang dalam ruang sempit (Human Rights Watch,
                            2016). Meskipun pemerintah sudah mengeluarkan Undang-Undang Kesehatan Jiwa sejak tahun 2014,
                            praktik pasung masih saja terjadi hingga sekarang. Hal ini disebabkan karena stigma
                            masyarakat mengenai orang dengan gangguan mental yang masih berakar. Tanpa mendapatkan
                            pertolongan, orang-orang yang dianggap sebagai “gila” tersebut justru diperlakukan dengan
                            tidak layak. Apakah Indonesia sudah melakukan upaya untuk mengatasi hal tersebut?</p>

                        <p class="card-text" style="text-align: left; color: black; font-size: 16px;">Undang-Undang no
                            18 tahun 2014 tentang Kesehatan Jiwa membuat definisi dari orang yang mengalami kesehatan
                            jiwa menjadi dua yaitu Orang Dengan Masalah Kejiwaan (ODMK) dan Orang Dengan Gangguan Jiwa
                            (ODGJ). OMDK adalah orang yang mempunyai gangguan di bagian fisik, mental, sosial,
                            pertumbahan dan perkembangan, dan kualitas hidup seperti depresi dan kecemasan yang lebih
                            bersifat prefentif sehingga beresiko untuk mengalami gangguan jiwa. ODGJ didefinisikan
                            sebagai orang yang mengalami gangguan pikiran, perilaku, dan perasaan yang termanifestasi
                            sehingga menjadi menderita dan terhambat fungsinya sebagai manusia seperti skizofrenia.</p>

                        <p class="card-text" style="text-align: left; color: black; font-size: 16px;">Pada tahun 2017,
                            Indonesia berada dalam ranking bawah dalam masalah penanganan kesehatan jiwa dibandingkan
                            negara-negara Asia Tenggara lainnya karena kurangnya jumlah Psikiatris yang hanya mencapai
                            hampir 900 Psikiatrir di Indonesia. Selain itu, Indonesia juga dihadapi dengan masalah
                            sedikitnya petugas kesehatan jiwa (Pols and Wibisono, 2017). Pemerintah menjanjikan 5% dari
                            APBN 2019 untuk diambil menjadi anggaran kesehatan dan hal ini merupakan peningkatan dimana
                            pada tahun 2017, anggaran kesehatan hanya 2.4% dari APBN dan 1% dari nilai tersebut dipakai
                            untuk kesehatan jiwa di Indonesia (Pols and Wibisono, 2017). Dengan alokasi anggaran yang
                            tidak banyak tersebut, terang saja bahwa masih banyak kepercayaan dan stigma akan kondisi
                            kesehatan jiwa di Indonesia. Human Rights Watch melaporkan bahwa di berbagai daerah di
                            Indonesia, kondisi kesehatan jiwa seseorang khususnya bagi ODGJ dipercayai berasal dari
                            kerasukan roh jahat, karena menjadi pendosa, melakukan perbuatan amoral, atau juga kurang
                            iman (Human Rights Watch, 2016). Karena kurangnya fasilitas layanan medis dan juga
                            kemiskinan di Indonesia, pengobatan tradisional seperti pergi ke dukun dan pengobatan
                            keagamaan menjadi pengobatan alternatif dimana mereka akan dipasung (Human Rights Watch,
                            2016).</p>

                        <p class="card-text" style="text-align: left; color: black; font-size: 16px;">Salah satu stigma
                            terbesar adalah bahwa orang yang masuk rumah sakit jiwa adalah orang-orang yang memiliki
                            gangguan mental sedangkan masalah kesehatan mental sangat beragam seperti depresi dan
                            kecemasan. Sering kali ODMK menutup diri karena diibilang “cari perhatian” atau “lebay” dan
                            mendapatkan perilaku negatif dari orang-orang sekitarnya sehingga mereka menutup diri dari
                            orang-orang (Soghoyan and Gasparyan, 2017). Menutup diri dapat mengakibatkan depresi yang
                            lebih tinggi dan dapat menjadi alasan untuk bunuh diri. Menurut World Health Organization,
                            ada sekitar 800,000 orang di dunia yang bunuh diri setiap tahunnya (World Health
                            Organization, 2018). Pada tahun 2018, Indonesia menduduki ranking ke-65 diantara 176 negara
                            lainnya dengan 2.9 kasus bunuh diri per 100,000 orang (Rakhmat and Tarahita, 2018). Menurut
                            Menteri Sosial ke-27, Khofifah Indar Parawansa, 40% dari anak-anak yang bunuh diri mengalami
                            kasus bullying (Rakhmat and Tarahita, 2018).</p>

                        <p class="card-text" style="text-align: left; color: black; font-size: 16px;">Indonesia perlu
                            meningkatkan kesadaran akan kesehatan mental. Edukasi dan sosialisasi akan hal ini penting
                            untuk mengubah stigma akan orang-orang dengan gangguan kesehatan jiwa dan untuk menutup
                            kemungkinan adanya stereotyping atau labelling yang dapat berdampak lebih bagi gangguan
                            tersebut (Hartini et al. 2018). Komunitas harus lebih erat lagi dan membuka diri untuk
                            cerita-cerita yang dialami anggotanya yang mengalami depresi. Peran pemerintah juga tidak
                            kalah besar. Pemerintah mungkin sudah membuat gerakan seperti “Indonesia Bebas Pasung 2014,
                            2019” dan sudah membuat aplikasi Sehat Jiwa yang merupakan kerjasama dengan World Health
                            Organization (WHO) untuk meningkatkan kesadaran akan kesehatan mental, namun pelaksanaan
                            akan Undang-Undang Kesehatan Jiwa harus diasah dan diperbaharui kembali. Pada akhirnya,
                            mereka semua adalah manusia dan adalah tanggung jawab kita sebagai suatu komunitas dan suatu
                            masyarakat untuk saling peduli satu sama lain.</p>

