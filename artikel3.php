                            dengan masalah dalam hidup dan bekerja, sehingga mereka lupa tidur dalam waktu yang cukup.
                            Penurunan jam tidur dapat menyebabkan suasana hati yang buruk, kurang konsentrasi, dan
                            masalah kesehatan lainnya. Tidur nyenyak setidaknya tujuh jam untuk menjaga kesehatan
                            mental.</p>

