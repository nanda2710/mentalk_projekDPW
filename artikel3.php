                            dan membagikan emosi kepada teman dan keluarga membantu melepaskan stres dan menenangkan
                            pikiran. Semakin kamu berinteraksi dan berbagi masalah dengan orang lain, maka semakin mudah
                            kamu menyingkirkan perasaan yang telah menyiksa.</p>

